from prettytable import PrettyTable
t = PrettyTable()
t.title = "第三方软件测试费计算"
t.field_names = ["序号","软件开发费用（M）（万元）","费率（%）"]
t.add_row(["一","M≤200","3.0"])
t.add_row(["二","200＜M≤500","2.5"])
t.add_row(["三","500＜M≤1000","2.0"])
t.add_row(["四","1000＜M≤2000","1.5"])
t.add_row(["五","2000＜M≤5000","1.0"])
t.add_row(["六","5000＜M","0.5"])
print(t)
x = int(input("请输入软件开发费用（万元）："))
print("软件开发费用：",x,"万元")
if x <= 200:
    y = x * 0.03
    print("第三方软件测试费 =",x,"* 3% =",y,"万元")
elif x <= 500:
    y = 200 * 0.03 + (x - 200) * 0.025
    print("第三方软件测试费 = 200 * 3% + (",x,"- 200 ) * 2.5% =",y,"万元")
elif x <= 1000:
    y = 200 * 0.03 + (500 - 200) * 0.025 + (x - 500) * 0.02
    print("第三方软件测试费 = 200 * 3% + ( 500 - 200 ) * 2.5% + (",x,"- 500 ) * 2% = ",y,"万元")
elif x <= 2000:
    y = 200 * 0.03 + (500 - 200) * 0.025 + (1000 - 500) * 0.02 + (x - 1000) * 0.015
    print("第三方软件测试费 = 200 * 3% + ( 500 - 200 ) * 2.5% + ( 1000 - 500 ) * 2% + (",x,"- 1000 ) * 1.5% =",y,"万元")
elif x <= 5000:
    y = 200 * 0.03 + (500 - 200) * 0.025 + (1000 - 500) * 0.02 + (2000 - 1000) * 0.015 + (x - 2000) * 0.01
    print("第三方软件测试费 = 200 * 3% + ( 500 - 200 ) * 2.5% + ( 1000 - 500 ) * 2% + ( 2000- 1000 ) * 1.5% + (",x,"- 2000 ) * 1% = ",y,"万元")
else:
    y = 200 * 0.03 + (500 - 200) * 0.025 + (1000 - 500) * 0.02 + (2000 - 1000) * 0.015 + (5000 - 2000) * 0.01 + (x - 5000) * 0.005
    print("第三方软件测试费 = 200 * 3% + ( 500 - 200 ) * 2.5% + ( 1000 - 500 ) * 2% + ( 2000- 1000 ) * 1.5% + ( 5000 - 2000 ) * 1% + (",x,"- 5000 ) * 5% =",y,"万元")
input('Press Enter to exit...')
